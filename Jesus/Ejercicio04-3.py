#programa que lee cuatro valores los suma y calcula su promedio
num1=int(input("Ingresa el primer valor "))
num2=int(input("Ingresa el segundo valor "))
num3=int(input("Ingresa el tercer valor "))
num4=int(input("Ingresa el cuarto valor "))

sumaTotal=num1+num2+num3+num4
promedio=sumaTotal/4

print("La suma de los valores es: ", sumaTotal)
print("El promedio de los valores es: ", promedio)

